package dto;

public class Reply {
	private int rid;
	private int qid;
	private String day;
	private String rcon;

	public int getRid() {
		return rid;
	}

	public void setRid(int rid) {
		this.rid = rid;
	}

	public int getQid() {
		return qid;
	}

	public void setQid(int qid) {
		this.qid = qid;
	}

	public String getDay() {
		return day;
	}

	public void setDay(String day) {
		this.day = day;
	}

	public String getRcon() {
		return rcon;
	}

	public void setRcon(String rcon) {
		this.rcon = rcon;
	}

}